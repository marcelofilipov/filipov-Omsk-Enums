package br.filipov.values.v;

/**
 * @author Marcelo A. Filipov
 * @since 2014
 */
public enum VisualizarQuantidadeValues {
	
	CINCO(5, "5"), DEZ(10, "10"), QUINZE(15, "15"), VINTE(20, "20");
	
	private Integer codigo;
	private String descricao;
	
	private VisualizarQuantidadeValues(Integer codigo, String descricao) {
		this.codigo = codigo;
		this.descricao = descricao;
	}

	public static String getDescricaoById(byte id) {
		String descricao = null;
		for (VisualizarQuantidadeValues v : values()) {
			if (v.getCodigo() == id) {
				descricao = v.getDescricao();
				break;
			}	
		}
		return descricao;
	}

	public static String getDescricaoById(short id) {
		String descricao = null;
		for (VisualizarQuantidadeValues v : values()) {
			if (v.getCodigo() == id) {
				descricao = v.getDescricao();
				break;
			}
		}
		return descricao;
	}

	public static String getDescricaoById(int id) {
		String descricao = null;
		for (VisualizarQuantidadeValues v : values()) {
			if (v.getCodigo() == id) {
				descricao = v.getDescricao();
				break;
			}
		}
		return descricao;
	}
	
	public static String getDescricaoById(long id) {
		String descricao = null;
		for (VisualizarQuantidadeValues v : values()) {
			if (v.getCodigo() == id) {
				descricao = v.getDescricao();
				break;
			}
		}
		return descricao;
	}
	
	public static VisualizarQuantidadeValues parse(int id){
		VisualizarQuantidadeValues retorno = null;
		for (VisualizarQuantidadeValues v: values()){
			if (v.getCodigo() == id) {
				retorno = v;
				break;
			}
		}
		return retorno;
	}

	public Integer getCodigo() {
		return codigo;
	}

	public String getDescricao() {
		return descricao;
	}

}
